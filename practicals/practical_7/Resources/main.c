/*
*   Function to configure system clock to 48 MHz
*/
void set_to_48MHz(void)
{
    RCC->CFGR |= (0b1010 << 18);                // Set PLL multiplier to 12
    RCC->CFGR |= RCC_CFGR_SW_PLL;               // Set PLL as system clock

    RCC->CR |= RCC_CR_PLLON;                    // Turn on PLL clock
}


